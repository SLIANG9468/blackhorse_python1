"""
算术运算符（7种）
+   -   *   /   //(取整数， 商到整数部分 9//2 = 4)    %(取余) **(指数)
"""
print("1+1", 1+1)
print("11//2", 11//2)   # 5
print("9%2", 9%2)       # 1
print("2**3", 2**3)     # 8

"""
复合赋值运算符
+=： c += a => c = c + a
-=： c -= a => c = c - a
*=： c *= a => c = c * a
/=： c /= a => c = c / a
%=： c %= a => c = c % a
**=： c **= a => c = c ** a
//=： c //= a => c = c // a
"""

num = 3
num %= 2
print("num %= 2: ", num)    # 1

num = 3
num **= 2
print("num **=", num)   # 9